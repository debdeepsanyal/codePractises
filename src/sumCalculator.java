public class sumCalculator {
    private double res;
    public double addRes(double first_num, double sec_num){
        res = first_num + sec_num;
        return res;
    }
    public double subRes(double first_num, double sec_num) {
        res = first_num - sec_num;
        return res;
    }
    public double mulRes(double first_num, double sec_num) {
        res = first_num * sec_num;
        return res;
    }
    public double divRes(double first_num, double sec_num) {
        res = first_num / sec_num;
        return res;
    }


}
